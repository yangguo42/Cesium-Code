/*
  响应式监听时钟播放状态
  监听 isPlaying 响应式变量的变化，当 Cesium 时钟从「暂停」变为「播放」时启动时间轴同步，
  从「播放」变为「暂停」时停止同步，实现播放 / 暂停状态与时间轴同步的自动联动
*/
